<?php

$dia = 6;
switch ($dia) {
	case $dia == 1:
		echo 'segunda-feira';
		break;

	case $dia == 2:
		echo 'terca-feira';
		break;

	case $dia == 3:
		echo 'quarta-feira';
		break;

	case $dia == 4:
		echo 'quinta-feira';
		break;

	case $dia == 5:
		echo 'sexta-feira';
		break;

	case 6:
		echo 'sabado';
		break;

	default:
		echo 'domingo';
		break;							
}

?>